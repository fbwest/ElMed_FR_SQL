	join D3_SCHET_OMS sc on zsl.D3_SCID = sc.ID and sc.YEAR = @year and sc.CODE_MO = @codemo
where sl.DS1 <> '' and sl.D3_ZSLID <> '' and sc.ID <> '' and floor(datediff(day, p.DR, sl.DATE_1) / 365.25) > 60
