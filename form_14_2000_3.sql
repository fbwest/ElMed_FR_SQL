declare @sampleT table (DS1 nvarchar(10), ZSLID int, RSLT int, FOR_POM int, VIDPOM int, KD int,
	NPOLIS nvarchar(20), DR datetime, DATE_1 datetime) 
insert into @sampleT
	select sl.DS1, sl.D3_ZSLID, zsl.RSLT, zsl.FOR_POM, zsl.VIDPOM, sl.KD, p.NPOLIS, p.DR, sl.DATE_1
	from D3_SL_OMS sl
		left join D3_ZSL_OMS zsl on zsl.ID = sl.D3_ZSLID
		left join D3_PACIENT_OMS p on p.ID = zsl.D3_PID
		join D3_SCHET_OMS sc on zsl.D3_SCID = sc.ID and sc.YEAR = @year and sc.CODE_MO = @codemo
	where sl.DS1 <> '' and sl.D3_ZSLID <> '' and sc.ID <> ''
		and zsl.USL_OK = 1
		and floor(datediff(day, p.DR, sl.DATE_1) / 365.25) <= 17

declare @form_sampleT table (dsName nvarchar(300), rowNum nvarchar(10), ds nvarchar(100),
	ZSLID int, RSLT int, FOR_POM int, VIDPOM int, KD int, NPOLIS nvarchar(20), DR datetime, DATE_1 datetime)
insert into @form_sampleT
	select dsName, rowNum, ds, ZSLID, RSLT, FOR_POM, VIDPOM, KD, NPOLIS, DR, DATE_1
	from @formT
		left join @sampleT on
			DS1 between dsFrom and dsTo
			or DS1 like dsLike
	union all
	select 'из них:<br>переломы', '20.1', 'S02, S12, S22, S32, S42, S52, S62, S72, S82, S92, T02, T08, T10, Т12, Т14.2',
		null, null, null, null, null, null, null, null
	union all
	select 'из них:<br>переломы', '20.1', 'S02, S12, S22, S32, S42, S52, S62, S72, S82, S92, T02, T08, T10, Т12, Т14.2',
		ZSLID, RSLT, FOR_POM, VIDPOM, KD, NPOLIS, DR, DATE_1
	from @sampleT
	where DS1 like 'S[0-9]2%'
		or DS1 like 'T0[28]%'
		or DS1 like 'T1[02]%'
		or DS1 like 'T14.2'

select dsName, rowNum, ds,
	count(distinct case when RSLT in (101,201) then NPOLIS end) as c22,
	count(distinct case when RSLT in (101,201) and FOR_POM = 1 then NPOLIS end) as c23,
	count(distinct case when RSLT in (101,201) and FOR_POM = 1 and VIDPOM in (2,21,201,202,203) then NPOLIS end) as c24,
